    private void jButton34ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton34ActionPerformed
        second = Double.parseDouble(CalS.getText());
        if (operetion == "+") {
            result = firstnum + second;
            anwser = String.format("%.0f", result);
            CalS.setText(anwser);
        } else if (operetion == "-") {
            result = firstnum - second;
            anwser = String.format("%.0f", result);
            CalS.setText(anwser);
        } else if (operetion == "/") {
            result = firstnum / second;
            anwser = String.format("%.02f", result);
            CalS.setText(anwser);
        } else if (operetion == "*") {
            result = firstnum * second;
            anwser = String.format("%.0f", result); 
            CalS.setText(anwser);
        }
    }//GEN-LAST:event_jButton34ActionPerformed

