    private void TOTALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TOTALActionPerformed
        int total = 0;
        double tax = 0;
        double dd = 350;
        LocalDate date = LocalDate.now();
        LocalTime time;
        time = LocalTime.now();
        try {
            if (AMALA.isSelected()) {
                total = total + 500;
                tax = tax + 1.;
            }
            if (RICE.isSelected()) {
                total = total + 700;
                tax = tax + 1.;
            }
            if (FUFU.isSelected()) {
                total = total + 600;
                tax = tax + 1.;
            }
            if (EBA.isSelected()) {
                total = total + 400;
                tax = tax + 1.;
            }
            DD.setText(Double.toString(dd));
            String ts = String.format("%.02f", tax);
            TS.setText(ts);
            OUTPUT.setText(Integer.toString(total));
            Double Total = total + tax + dd;
            String tt = String.format("%.02f", Total);
            Date dt = new Date();
            int hour = dt.getHours();
            if (hour >= 1 && hour < 12) {
                RECIEPT.setText("GOOD MORNING \n YOUR TOTAL COST IS " + "#" + tt);
            }
            if (hour >= 12 && hour < 16) {
                RECIEPT.setText("GOOD AFTERNOON \n YOUR TOTAL COST IS " + "#" + tt);
            }
            if (hour >= 21 && hour <= 24) {
                RECIEPT.setText("GOOD NIGHT YOUR TOTAL COST IS " + "#" + tt);
            }
            if (hour >= 16 && hour < 21) {
                RECIEPT.setText("GOOD EVENING \n YOUR TOTAL COST IS " + "#" + tt);
            }

            TIMEj.setText(" DATE " + date + " TIME " + time);//dat.setText(Integer.toString(hour));
        } catch (Exception e) {
        }        // TODO add your handling code here:
    }//GEN-LAST:event_TOTALActionPerformed

